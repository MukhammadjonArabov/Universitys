def get_test_questions(lang='uz'):
    """
    Get test questions in the specified language.
    
    Args:
        lang: Language code ('uz', 'ru', 'en')
        
    Returns:
        List of questions with text in the specified language
    """
    if lang not in ('uz', 'ru', 'en'):
        lang = 'uz'
    
    localized_questions = []
    
    for question in TEST_QUESTIONS:
        # Get question text in target language
        q_text = question.get('text', {}).get(lang, question.get('text', {}).get('uz', ''))
        
        # Localize options
        localized_options = []
        for option in question.get('options', []):
            opt_text = option.get('text', {}).get(lang, option.get('text', {}).get('uz', ''))
            localized_options.append({
                'id': option.get('id'),
                'text': opt_text,
                'direction': option.get('direction')
            })
        
        localized_questions.append({
            'id': question.get('id'),
            'text': q_text,
            'options': localized_options
        })
    
    return localized_questions
